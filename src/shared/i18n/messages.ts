export type { MessageKey };

export function translate(
  locale: AppLocale,
  key: MessageKey,
  vars?: Record<string, string>,
): string {
  const raw = locale === "en" ? en[key] : fr[key];
  if (!vars) return raw;
  return Object.entries(vars).reduce(
    (acc, [k, v]) => acc.split(`{${k}}`).join(v),
    raw,
  );
}
